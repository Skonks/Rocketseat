/* 
    Operadores de comparação
    Irá comparar valores retornando um booleano como resposta
*/

let one = 1
let two = 2

// == igual a 
console.log(two == 1)
console.log(one == "1")

// != diferente de
console.log(one != two)
console.log(one != 1)
console.log(one != "1")

/* 
    Operadores de comparação estritamente igual
*/

// === estritamente igual a
console.log(one === "1")
console.log(one === 1)

// !== estritamente diferente de
console.log(two !== "2")
console.log(two !== 2)