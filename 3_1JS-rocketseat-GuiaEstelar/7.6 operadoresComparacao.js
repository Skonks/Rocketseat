/* 
    Operadores de comparação
    Irá comparar valores retornando um booleano como resposta
*/

let one = 1
let two = 2

// == igual a 
console.log(two == 1)
console.log(one == "1")

// != diferente de
console.log(one != two)
console.log(one != 1)
console.log(one != "1")

/* 
    Operadores de comparação estritamente igual
*/

// === estritamente igual a
console.log(one === "1")
console.log(one === 1)

// !== estritamente diferente de
console.log(two !== "2")
console.log(two !== 2)


/* 
    Operadores de comparação maior e menor (igual)
*/

let um = 1
let dois = 2

// > maior que
console.log(um > dois)

// >= maior ou igual a
console.log(um >= 1)
console.log(two >= 1)

// < menor que
console.log(one < two)

// <= menor ou igual a
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)